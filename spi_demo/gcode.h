#ifndef GCODE_H_
#define GCODE_H_

#include "L6472.h"

/****************************************************************************
 * G0: Rapid positioning
 ****************************************************************************/
// Set f (speed) paramter
void G0_f( float f );

// Execute move to position (x,y)
void G0_xy( float x, float y );


/****************************************************************************
 * G1: Linear interpolation
 ****************************************************************************/
// Set f (speed) paramter
void G1_f( float f );

// Execute move to position (x,y)
void G1_xy( float x, float y );


/****************************************************************************
 * G2: Clockwise arc
 ****************************************************************************/
// Program in MMs
void G2_xyij( float x, float y, float i, float j );


/****************************************************************************
 * G4: Dwell
 ****************************************************************************/
// Program in MMs
void G4_p( float p );


/****************************************************************************
 * G21: Linear interpolation
 ****************************************************************************/
// Program in MMs
void G21( void );

/****************************************************************************
 * G90: Absolute position
 ****************************************************************************/
// Set origin to current location
void G90( void );


/****************************************************************************
 * M3: Laser on
 ****************************************************************************/
// Turn on laser
void M3_s( uint8_t s );


/****************************************************************************
 * M5: Laser off
 ****************************************************************************/
// Turn off laser
void M5_s( uint8_t s );


/****************************************************************************
 * M18: Disable stepper motors
 ****************************************************************************/
// Release x and y motor bridges
void M18( void );

#endif /* GCODE_H_ */



